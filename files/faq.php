                        In der ersten Sitzung erhalten die Parteien eine kurze Erklärung über Mediation und den Ablauf des Verfahrens. Es werden die Rahmenbedingungen, die Kosten und der weitere Ablauf besprochen. Über Auftrag, Inhalte und Kostenteilung wird eine Vereinbarung geschlossen. Bereits hier gilt absolute gegenseitige Vertraulichkeit.
                        <br><br>
                        Der Mediationsprozess ist klar strukturiert und orientiert sich an einem 5-Phasen Modell. Meine Aufgabe als Mediatorin ist die Moderation der Gespräche und die Begleitung durch den Mediationsprozess. Mit Beginn der Mediation lassen sich die Klient*innen auf einen geführten Prozess ein, in dem jede Seite ihre jeweiligen Themen ansprechen und vorbringen kann und darf. Unterschiedliche Sichtweisen werden dargestellt und Bedürfnisse ausgesprochen. Daran schließen
                        die gemeinsame Arbeit an der Lösungsfindung und mögliche Verhandlungen an, mit dem Ziel, am Ende eine Abschlussvereinbarung – die Mediationsvereinbarung — zu finden. <br><br>
                        <br><br>
