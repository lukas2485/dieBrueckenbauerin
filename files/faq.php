                        <p class="answer p-top">
                        
                        Die Mediation ist ein (außergerichtliches) Verfahren zur Konfliktbeilegung unter Zuhilfenahme eines allparteiischen Dritten (Mediator*in). Das ermöglicht es den Beteiligten eine von allen akzeptierte gemeinsame Problemlösung zu erarbeiten.
